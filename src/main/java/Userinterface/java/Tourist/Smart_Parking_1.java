    private void submitButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButton1MouseClicked
        if(NameField.getText().isEmpty() || emailField.getText().isEmpty()||numberField2.getText().isEmpty()||imageName.getText().isEmpty())
        {
            error.setText("Fields empty");
            return;
        }
        ParkingRequest hr = new ParkingRequest(Database.loggedIn.getUsername(),null,null,null);
        ObjectSet<ParkingRequest> h = Database.db.get(hr);
        if(h.isEmpty())
        {
            ParkingRequest new_hr = new ParkingRequest(NameField.getText(),emailField.getText(),numberField2.getText(),"");
            error.setText("Request successfully Submitted");
            Database.db.store(new_hr);
        }
    }//GEN-LAST:event_submitButton1MouseClicked

